#!/bin/bash

# Запрашиваем массу тела
echo "Введите вашу массу (в кг):"
read MASS

# Запрашиваем рост
echo "Введите ваш рост (в метрах, например 1.75):"
read HEIGHT

# Вычисляем ИМТ (масса / рост²) и округляем до целого числа
BMI=$(echo "$MASS / ($HEIGHT * $HEIGHT)" | bc | awk '{printf "%.0f", $0}')

# Выводим результат
echo "Ваш индекс массы тела (ИМТ): $BMI"
