choice = input("Куда поедешь? (налево/направо/прямо): ").strip().lower()

if choice == "налево" or choice == "л":
    print("Ты потерял коня, но добрался до дома пешком.")
elif choice == "направо" or choice == "п":
    print("Ты сложил голову в чистом поле. Погиб как герой.")
elif choice == "прямо":
    print("Ты встретил свою судьбу и обрел семью.")
else:
    print("Так и стоишь на распутье. Время уходит...")